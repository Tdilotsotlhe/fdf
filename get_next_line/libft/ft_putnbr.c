#include "libft.h"

void	ft_putnbr(int n)
{
	char	ret;

	if (n == -2147483648)
		ft_putstr("-2147483648");
	else if (n < 0)
	{
		ft_putchar('-');
		ft_putnbr(-n);
	}
	else if (n >= 10)
	{
		ft_putnbr(n / 10);
		ret = n % 10 + '0';
		ft_putchar(ret);
	}
	else if (n <= 9)
	{
		ft_putchar(n + 48);
	}
}
