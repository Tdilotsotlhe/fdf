#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char	ret;

	if (n == -2147483648)
		ft_putstr_fd("-2147483648", fd);
	else if (n < 0)
	{
		ft_putchar_fd('-', fd);
		ft_putnbr_fd(-n, fd);
	}
	else if (n >= 10)
	{
		ft_putnbr_fd(n / 10, fd);
		ret = n % 10 + '0';
		ft_putchar_fd(ret, fd);
	}
	else if (n <= 9)
	{
		ft_putchar_fd(n + 48, fd);
	}
}
