#include "libft.h"

void	ft_strclr(char *s)
{
	size_t w;
	size_t i;

	i = 0;
	if (s == NULL)
		return ;
	w = ft_strlen(s);
	if (!s)
	{
		return ;
	}
	if (s)
	{
		while (i < w)
		{
			s[i++] = '\0';
		}
	}
	while (i < w)
	{
		s[i++] = '\0';
	}
}
